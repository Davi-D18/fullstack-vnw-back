# Tipos de Variáveis

# String
nome = 'Amanhã tem aula, ao meio dia'
print(type(nome))

# Inteiro
idade = 20
print(type(idade))

# Float
altura = 1.80
print(type(altura))

# Booleano
ativo = True
print(type(ativo))

# None
nada = None
print(type(nada))

# Listas
nomes = ['João', 'Maria', 'Pedro']
print(type(nomes))

# Dicionarios
pessoa = {
  'nome': 'João',
  'idade': 20,
  'altura': 1.80
}
print(type(pessoa))

# Tuplas
coordenadas = (10, 20)
print(type(coordenadas))

# Conjuntos
nomes = {'João', 'Maria', 'Pedro'}
print(type(nomes))